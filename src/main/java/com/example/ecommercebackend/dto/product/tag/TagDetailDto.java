package com.example.ecommercebackend.dto.product.tag;

public class TagDetailDto {
    private int id;
    private String name;

    public TagDetailDto(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
